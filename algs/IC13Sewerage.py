class IC13Sewerage(QgsProcessingAlgorithm):
    """
    Mide el porcentaje de hogares con conexión domiciliaria al sistema de
    alcantarillado, por tejido urbano.
    El número de hogares con conexión domiciliaria se divide para el total de hogares,
    expresado en porcentaje. 
    Formula: (Viviendas con conexión a alcantarillado / Total de viviendas)*100
    """
    SEWERAGE = 'SEWERAGE'
    BLOCKS = 'BLOCKS'
    FIELD_HOUSING = 'FIELD_HOUSING'
    CELL_SIZE = 'CELL_SIZE'    
    OUTPUT = 'OUTPUT'
    STUDY_AREA_GRID = 'STUDY_AREA_GRID'    

    def initAlgorithm(self, config):
        currentPath = getCurrentPath(self)
        FULL_PATH = buildFullPathName(currentPath, nameWithOuputExtension(NAMES_INDEX['IC13'][1]))            

        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.BLOCKS,
                self.tr('Manzanas'),
                [QgsProcessing.TypeVectorPolygon]
            )
        )


        self.addParameter(
            QgsProcessingParameterField(
                self.FIELD_HOUSING,
                self.tr('Viviendas'),
                'viviendas', 'BLOCKS'
            )
        )         

        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.SEWERAGE,
                self.tr('Cobertura alcantarillado)'),
                [QgsProcessing.TypeVectorAnyGeometry]
            )
        )


        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.STUDY_AREA_GRID,
                self.tr(TEXT_GRID_INPUT),
                [QgsProcessing.TypeVectorPolygon],
                '', OPTIONAL_GRID_INPUT
            )
        )        

        if OPTIONAL_GRID_INPUT:
            self.addParameter(
                QgsProcessingParameterNumber(
                    self.CELL_SIZE,
                    self.tr('Tamaño de la malla'),
                    QgsProcessingParameterNumber.Integer,
                    P_CELL_SIZE, False, 1, 99999999
                )
            )        



        self.addParameter(
            QgsProcessingParameterFeatureSink(
                self.OUTPUT,
                self.tr('Salida'),
                QgsProcessing.TypeVectorAnyGeometry,
                str(FULL_PATH)                
            )
        )

    def processAlgorithm(self, params, context, feedback):
        totalStpes = 13
        fieldHousing = params['FIELD_HOUSING']

        feedback = QgsProcessingMultiStepFeedback(totalStpes, feedback)


        steps = 0 + 1
        feedback.setCurrentStep(steps)
        if not OPTIONAL_GRID_INPUT: params['CELL_SIZE'] = P_CELL_SIZE
        grid, isStudyArea = buildStudyArea(params['CELL_SIZE'], params['BLOCKS'],
                                           params['STUDY_AREA_GRID'],
                                           context, feedback)
        gridNeto = grid      


        steps += 1
        feedback.setCurrentStep(steps)
        blocks = calculateArea(params['BLOCKS'], 'area_bloc', context,
                               feedback)


        steps += 1
        feedback.setCurrentStep(steps)
        segments = intersection(
            blocks['OUTPUT'],
            gridNeto['OUTPUT'],
            f'area_bloc;{fieldHousing}',
            'id_grid',
            context,
            feedback,
        )


        steps += 1
        feedback.setCurrentStep(steps)
        segmentsArea = calculateArea(segments['OUTPUT'],
                                     'area_seg',
                                     context, feedback)

        steps += 1
        feedback.setCurrentStep(steps)
        formulaHousingSegments = f'(area_seg/area_bloc) * {fieldHousing}'
        housingForSegments = calculateField(segmentsArea['OUTPUT'], 'hou_seg',
                                            formulaHousingSegments,
                                            context,
                                            feedback)

        steps += 1
        feedback.setCurrentStep(steps)
        blocksWithId = calculateField(housingForSegments['OUTPUT'], 'id_block', '$id', context,
                                      feedback, type=1)

        steps += 1
        feedback.setCurrentStep(steps)
        seweragetWithId = calculateField(params['SEWERAGE'], 'idx', '$id', context,
                                        feedback, type=1)      


        steps += 1
        feedback.setCurrentStep(steps)
        blocksWithSewerage = joinByLocation(blocksWithId['OUTPUT'],
                                          seweragetWithId['OUTPUT'],
                                          'idx', [INTERSECTA], [COUNT],
                                          UNDISCARD_NONMATCHING,
                                          context,
                                          feedback)


        """
      -----------------------------------------------------------------
      Calcular numero de viviendas por hexagano
      -----------------------------------------------------------------
      """




          # Haciendo el buffer inverso aseguramos que los segmentos
          # quden dentro de la malla
        steps += 1
        feedback.setCurrentStep(steps)
        sewerageForSegmentsFixed = makeSureInside(blocksWithSewerage['OUTPUT'],
                                                    context,
                                                    feedback)
          # Con esto se saca el total de viviendas
        steps += 1
        feedback.setCurrentStep(steps)
        gridNetoAndSegments = joinByLocation(gridNeto['OUTPUT'],
                                             sewerageForSegmentsFixed['OUTPUT'],
                                             'idx_count;hou_seg',
                                             [CONTIENE], [SUM], UNDISCARD_NONMATCHING,                 
                                             context,
                                             feedback)

          #descartar NULL para obtener el total de viviendas que cumple
        steps += 1
        feedback.setCurrentStep(steps)
        sewerageNotNullForSegmentsFixed = filter(sewerageForSegmentsFixed['OUTPUT'],
                                                   'idx_count', NOT_NULL,
                                                   '', context, feedback)

        steps += 1
        feedback.setCurrentStep(steps)
        gridNetoAndSegmentsNotNull = joinByLocation(gridNetoAndSegments['OUTPUT'],
                                                    sewerageNotNullForSegmentsFixed['OUTPUT'],
                                                    'hou_seg',
                                                    [CONTIENE], [SUM], UNDISCARD_NONMATCHING,               
                                                    context,
                                                    feedback)

        steps += 1
        feedback.setCurrentStep(steps)
        formulaSewer = 'coalesce((coalesce(hou_seg_sum_2,0) /  coalesce(hou_seg_sum,""))*100, "")'
        return calculateField(
            gridNetoAndSegmentsNotNull['OUTPUT'],
            NAMES_INDEX['IC13'][0],
            formulaSewer,
            context,
            feedback,
            params['OUTPUT'],
        )

        # Return the results of the algorithm. In this case our only result is
        # the feature sink which contains the processed features, but some
        # algorithms may return multiple feature sinks, calculated numeric
        # statistics, etc. These should all be included in the returned
        # dictionary, with keys matching the feature corresponding parameter
