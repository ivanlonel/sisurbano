        isNoiseDay = isValid(params['NOISE_DAY'])    
        isNoiseNight = isValid(params['NOISE_NIGHT'])    


        if isBlocks and isFieldPopulation and isNoiseDay and isNoiseNight:
            # B03 Confort acústico
            steps = steps+1
            feedback.setCurrentStep(steps)  
