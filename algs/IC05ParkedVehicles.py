class IC05ParkedVehicles(QgsProcessingAlgorithm):
    """
    Mide de forma indirecta la intensidad de uso del vehículo y la cobertura de
    sistemas de transporte público. Área destinada a parqueaderos en el espacio
    público con respecto a la superficie del tramo de calle.
    Formula:(Superficie de parqueadero en m2 / Superficie total del tramo de calle en m2)*100
    """
    ROADS = 'ROADS'
    PARKING = 'PARKING'
    AREA_PER_PARKING = 'AREA_PER_PARKING'
    CELL_SIZE = 'CELL_SIZE'    
    OUTPUT = 'OUTPUT'
    STUDY_AREA_GRID = 'STUDY_AREA_GRID'    

    def initAlgorithm(self, config):
        currentPath = getCurrentPath(self)
        FULL_PATH = buildFullPathName(currentPath, nameWithOuputExtension(NAMES_INDEX['IC05'][1]))            

        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.ROADS,
                self.tr('Vías públicas'),
                [QgsProcessing.TypeVectorPolygon]
            )
        )

        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.PARKING,
                self.tr('Puestos de parqueo'),
                [QgsProcessing.TypeVectorPoint]
            )
        ) 

        self.addParameter(
            QgsProcessingParameterNumber(
                self.AREA_PER_PARKING,
                self.tr('Área de cada parqueadero'),
                QgsProcessingParameterNumber.Integer,
                10, False, 1, 99999999
            )
        )    

        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.STUDY_AREA_GRID,
                self.tr(TEXT_GRID_INPUT),
                [QgsProcessing.TypeVectorPolygon],
                '', OPTIONAL_GRID_INPUT
            )
        )        

        if OPTIONAL_GRID_INPUT:
            self.addParameter(
                QgsProcessingParameterNumber(
                    self.CELL_SIZE,
                    self.tr('Tamaño de la malla'),
                    QgsProcessingParameterNumber.Integer,
                    P_CELL_SIZE, False, 1, 99999999
                )
            )         

        self.addParameter(
            QgsProcessingParameterFeatureSink(
                self.OUTPUT,
                self.tr('Salida'),
                QgsProcessing.TypeVectorAnyGeometry,
                str(FULL_PATH)                
            )
        )

    def processAlgorithm(self, params, context, feedback):
        steps = 0
        totalStpes = 10
        areaPerParking = str(params['AREA_PER_PARKING'])

        feedback = QgsProcessingMultiStepFeedback(totalStpes, feedback)

        feedback.setCurrentStep(steps)
        if not OPTIONAL_GRID_INPUT: params['CELL_SIZE'] = P_CELL_SIZE
        grid, isStudyArea = buildStudyArea(params['CELL_SIZE'], params['ROADS'],
                                           params['STUDY_AREA_GRID'],
                                           context, feedback)
        gridNeto = grid  


          # CALCULAR VIARIO
        steps += 1
        feedback.setCurrentStep(steps)
        segments = intersection(params['ROADS'], gridNeto['OUTPUT'],
                                [],
                                'id_grid;area_grid',
                                context, feedback)

        steps += 1
        feedback.setCurrentStep(steps)
        segmentsArea = calculateArea(segments['OUTPUT'], 'area_road', context,
                                     feedback)

        steps += 1
        feedback.setCurrentStep(steps)
        segmentsFixed = makeSureInside(segmentsArea['OUTPUT'],
                                       context,
                                       feedback)

        steps += 1
        feedback.setCurrentStep(steps)
        gridNetoAndSegments = joinByLocation(gridNeto['OUTPUT'],
                                             segmentsFixed['OUTPUT'],
                                             [],
                                             [CONTIENE], [SUM],    
                                             UNDISCARD_NONMATCHING,                               
                                             context,
                                             feedback)

          # CONTAR Y CALCULAR DE LOS PARQUEOS

        steps += 1
        feedback.setCurrentStep(steps)
        parkingWithId = calculateField(params['PARKING'], 'idx', '$id', context,
                                        feedback, type=1)      




        steps += 1
        feedback.setCurrentStep(steps)
        gridNetoAndSegmentsSumLinesLum = joinByLocation(gridNetoAndSegments['OUTPUT'],
                                             parkingWithId['OUTPUT'],
                                             ['idx'],
                                             [CONTIENE], [COUNT],    
                                             UNDISCARD_NONMATCHING,                               
                                             context,
                                             feedback)      



        steps += 1
        feedback.setCurrentStep(steps)
        formulaParkingRoad = (
            f'coalesce((idx_count*{areaPerParking})/(area_road_sum), "")'
        )
        return calculateField(
            gridNetoAndSegmentsSumLinesLum['OUTPUT'],
            NAMES_INDEX['IC05'][0],
            formulaParkingRoad,
            context,
            feedback,
            params['OUTPUT'],
        )


        # Return the results of the algorithm. In this case our only result is
        # the feature sink which contains the processed features, but some
        # algorithms may return multiple feature sinks, calculated numeric
        # statistics, etc. These should all be included in the returned
        # dictionary, with keys matching the feature corresponding parameter
        # or output names.
